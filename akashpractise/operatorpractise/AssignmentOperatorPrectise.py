# Assignment Operator
num1 = int(input("Enter Number:- "))
num2 = int(input("Enter 2nd Number: - "))

num1 += num2
print("+=",num1)

num1 -= num2
print("-=",num1)

num1 *= num2
print("*=",num1)

num1 /= num2
print("/=",num1)

num1 **= num2
print("**=",num1)

num1 //= num2
print("//=",num1)

num1 %= num2
print("%=",num1)